public class Shape {
	// Shape 클래스 시작
	double x; // x좌표를 저장할 변수 선언
	double y; // y좌표를 저장할 변수 선언
	public Shape(double x, double y) {
		// parameter가 2개인 Shape 생성자 시작
		this.x = x; // 입력받은 x를 필드 x에 저장
		this.y = y; // 입력받은 y를 필드 y에 저장
	} // Shape 생성자 끝

} // Shape 클래스 끝
